public class Hello{
    public static void main(String[] args) {
        System.out.println("Hello, World!");
        int num = 10;
        System.out.println("The value of num is: " + num);
        float pi = 3.14f;
        System.out.println("The value of pi is: " + pi);
        char letter = 'A';
        System.out.println("The value of letter is: " + letter);
        boolean b = true;
        System.out.println("The value of b is: " + b);
        int bin = 0b1010; // binary literal for 10
        System.out.println("The value of bin is: " + bin);
        int hex = 0x1A; // hexadecimal literal for 26
        System.out.println("The value of hex is: " + hex);
        double d = 12e10; // scientific notation for 12 * 10^10
        System.out.println("The value of d is: " + d);
    }
}